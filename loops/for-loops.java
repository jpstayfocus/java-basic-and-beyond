import java.awt.*;
import java.util.Arrays;

public class Main {
    public static void main(String[] args) {
        // for loops
        System.out.println("Start of for loop");
        for (int i = 0; i <= 10; i++) {
            System.out.println("Hello " + i);
        }
        System.out.println("End of for loop");

        System.out.println("*******************");
        System.out.println("reverse for loop");
        for (int i = 10; i >= 0; i--) {
            System.out.println("Hello " + i);
        }
    }
}
