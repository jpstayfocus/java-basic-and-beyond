
public class Main {
    public static void main(String[] args) {

        // comparison Operators
        // <, <=, >, >=, ==, !=
        System.out.println(10 < 10);
        System.out.println(10 > 10);
        System.out.println(10 <= 10);
        System.out.println(10 <= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);

        System.out.println();

        boolean isAdult = 15 >= 16;
        System.out.println(isAdult);

    }
}
