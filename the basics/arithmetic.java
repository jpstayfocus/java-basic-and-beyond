public class Main {
    public static void main(String[] args) {
        int numberOne =  10;
        int numberTwo = 3;
        System.out.println(numberOne + numberTwo);
        System.out.println(numberOne - numberTwo);
        System.out.println(numberOne * numberTwo);
        System.out.println(numberOne / numberTwo);
        System.out.println(numberOne % numberTwo);
        // BODMAS
        // B- Brackets
        // O- Orders (powers/indices or roots)
        // D- Division
        // M- Multiplication
        // A- Addition
        // S- Subtraction
        System.out.println((2+2) * (3/1) * 2);
    }
}
